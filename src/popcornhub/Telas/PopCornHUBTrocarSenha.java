    private void jButtonAlterarSenhaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonAlterarSenhaMouseClicked
        // TODO add your handling code here:
        Usuario u = new Usuario();
        u.setUsuario(jTextFieldUsuarioAlterarSenha.getText());
        u.setPassword(new String(jPasswordFieldAlterarSenha.getPassword()));
        u.setPin_seguranca(Integer.parseInt(jTextFieldPindeSeg.getText()));
        
        if(verificarCampos()){
            
            String query = "UPDATE `usuarios` SET `username` = '" + u.usuario +"', `password`='" + u.password + "' WHERE pin_seg = " + u.pin_seguranca + " ";
            
            Connection con = ConnectionFactory.getConnection();
            PreparedStatement stmt = null;
            try {
                
                stmt = con.prepareStatement(query);
                stmt.execute();
                JOptionPane.showMessageDialog(null, "Informações Atualizadas");
                chamarTelaLogin();
            } catch (SQLException ex) {
                Logger.getLogger(PopCornHUBTrocarSenha.class.getName()).log(Level.SEVERE, null, ex);
            } finally{
                ConnectionFactory.closeConnection(con, stmt);
            }
            
            
            
        }
        
    }//GEN-LAST:event_jButtonAlterarSenhaMouseClicked

