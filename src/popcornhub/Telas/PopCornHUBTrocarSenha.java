    public boolean verificarCampos(){
        
        if(jTextFieldUsuarioAlterarSenha.getText().trim().equals("") || jPasswordFieldAlterarSenha.getText().trim().equals("") ||
                jTextFieldPindeSeg.getText().trim().equals("")){
            JOptionPane.showMessageDialog(null,"Você deixou algum campo vazio. A Alteração foi barrada","Campos Vazios",2);
            return false;
        } else{
            return true;
        }
        
    }
    
