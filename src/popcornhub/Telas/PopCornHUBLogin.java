    public boolean checkUsername(String username){
        
        PreparedStatement stmt;
        ResultSet rs;
        boolean username_exist = true;
        
        String query = "SELECT * FROM `usuarios` WHERE `username` = ?";
        
        try {
            stmt = ConnectionFactory.getConnection().prepareStatement(query);
            stmt.setString(1, username);
            rs = stmt.executeQuery();
            
            if(rs.next())
            {
                username_exist = true;
            }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro: " +ex);
        }
        
        return username_exist;
        
    }
    
    public boolean verifyFields(){
        String user = jTextFieldUsuarioLogin.getText();
        String pass = new String(jPasswordFieldLogin.getPassword());
        
        // Checa se há campos vazios
        if(user.trim().equals("") || pass.trim().equals("")){
            JOptionPane.showMessageDialog(null, "Um ou mais campos estão vazios", "Campos vazios", 2);
            return false;
        } else {
            return true;
        }
    }

