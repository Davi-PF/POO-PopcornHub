    private void jButtonEntrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonEntrarMouseClicked
        // TODO add your handling code here:
        
        Usuario u = new Usuario();
        u.setUsuario(jTextFieldUsuarioLogin.getText());
        u.setPassword(new String(jPasswordFieldLogin.getPassword()));
        System.out.println("Usuario = " + u.usuario);
        
        if(verifyFields()){
            if(checkUsername(u.getUsuario())){
                Connection con = ConnectionFactory.getConnection();
                PreparedStatement stmt = null;
                ResultSet rs;
        
            String query = "SELECT * FROM `usuarios` WHERE `username` = ? AND `password` = ?";
            if(u.usuario.trim().equals("")){ 
                JOptionPane.showMessageDialog(null, "Digite o seu Usuário","Usuário Vazio",2);
            } else if(u.password.trim().equals("")){
                JOptionPane.showMessageDialog(null, "Digite a sua Senha","Senha Vazia",2);
            }else {
                try{
                    stmt = con.prepareStatement(query);
                    stmt.setString(1, u.usuario);
                    stmt.setString(2, u.password);
                    rs = stmt.executeQuery();
                    
                    if(rs.next()){
                        // mostra um novo form
                        PopCornHUBTelaUsuario tu = new PopCornHUBTelaUsuario();
                        tu.setVisible(true);
                        tu.pack();
                        tu.setLocationRelativeTo(null);
                
                        this.dispose();
                    } else{
                        // mensagem de erro
                        JOptionPane.showMessageDialog(null, "Usuário / Senha Inválido", "Login Error",2);
                    }
                } catch (SQLException ex){
                    JOptionPane.showMessageDialog(null, "Erro: "+ ex);
                } finally{
                    ConnectionFactory.closeConnection(con, stmt);
                }
            }
        }
            
        }    
    }//GEN-LAST:event_jButtonEntrarMouseClicked

