    private void jButtonPesquisarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonPesquisarMouseClicked
        // TODO add your handling code here:
        
        Connection con = ConnectionFactory.getConnection();
        String pesquisar_nome = jTextFieldPesquisar.getText();
        String posicao_combo = (String) jComboBoxPesquisarGenero.getSelectedItem();
        String query = null;
        PreparedStatement stmt = null;
        ResultSet rs;
        
        if(posicao_combo.trim().equals("Selecione um gênero")){
            query = "SELECT m.movie_id, title, GROUP_CONCAT(genre_name ORDER BY genre_name SEPARATOR ', ') AS genre, runtime, tempo_assistido \n" +
"	FROM \n" +
"		movie m, lista_filmes lf, usuarios u, movie_genres mg, genre g\n" +
"			WHERE \n" +
"				m.title LIKE '%"+ pesquisar_nome +"%' " +
                "             AND (lf.cod_usuario_lf = u.cod_usuario AND lf.movie_id_lf = m.movie_id AND (mg.movie_id = m.movie_id AND mg.genre_id = g.genre_id))\n" +
"				GROUP BY m.movie_id;";

        } else{
            query = "SELECT m.movie_id, title, GROUP_CONCAT(genre_name ORDER BY genre_name SEPARATOR ', ') AS genre, runtime, tempo_assistido \n" +
"	FROM \n" +
"		movie m, lista_filmes lf, usuarios u, movie_genres mg, genre g\n" +
"			WHERE \n" +
"				m.title LIKE '%"+ pesquisar_nome +"%' AND (g.genre_name = '"+ posicao_combo +
                "'             AND (lf.cod_usuario_lf = u.cod_usuario AND lf.movie_id_lf = m.movie_id AND (mg.movie_id = m.movie_id AND mg.genre_id = g.genre_id)))\n" +
"				GROUP BY m.movie_id;";

        }
                
        try{

            stmt = con.prepareStatement(query);
            rs = stmt.executeQuery();
            DefaultTableModel tm = (DefaultTableModel)jTableTabelaUsuario.getModel();
            tm.setRowCount(0);
        
            while(rs.next()){
                Object o[] = {rs.getInt("movie_id"), rs.getString("title"),rs.getString("genre"),rs.getFloat("runtime"),
                rs.getFloat("tempo_assistido")};
                tm.addRow(o);
            }
        } catch(Exception e){
            JOptionPane.showMessageDialog(null,"Erro: "+e);
        }
        
    }//GEN-LAST:event_jButtonPesquisarMouseClicked

    private void jButtonAlterarTempoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonAlterarTempoMouseClicked
        // TODO add your handling code here:
        
        String tempo_assistido = jTextFieldTempoAssistido.getText();
        int id_filme = Integer.parseInt(jLabelID.getText());
        
        if(tempo_assistido.trim().equals("")){
            tempo_assistido = "0";
        }
        
        String query = "UPDATE `lista_filmes` SET `tempo_assistido` = " + Float.parseFloat(tempo_assistido) + "WHERE movie_id_lf = "+ id_filme +"; ";
            
            Connection con = ConnectionFactory.getConnection();
            PreparedStatement stmt = null;
            try {
                
                stmt = con.prepareStatement(query);
                stmt.execute();
                JOptionPane.showMessageDialog(null, "Informações Atualizadas");
                atualizarTabela();
            } catch (SQLException ex) {
                Logger.getLogger(PopCornHUBTrocarSenha.class.getName()).log(Level.SEVERE, null, ex);
            } finally{
                ConnectionFactory.closeConnection(con, stmt);
            }
        
    }//GEN-LAST:event_jButtonAlterarTempoMouseClicked

