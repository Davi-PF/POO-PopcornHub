    private void jTableTabelaUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableTabelaUsuarioMouseClicked
        // TODO add your handling code here:
        PreparedStatement stmt = null;
        Connection con = ConnectionFactory.getConnection();
        
        try{
            int row = jTableTabelaUsuario.getSelectedRow();
            int Table_click = Integer.parseInt((jTableTabelaUsuario.getModel().getValueAt(row, 0).toString()));

            String sql = "SELECT m.movie_id, title, GROUP_CONCAT(genre_name ORDER BY genre_name SEPARATOR ', ') AS genre, runtime, tempo_assistido \n" +
"	FROM \n" +
"		movie m, lista_filmes lf, usuarios u, movie_genres mg, genre g\n" +
"			WHERE \n" +
"				m.movie_id = "+ Table_click + " AND (lf.cod_usuario_lf = u.cod_usuario AND lf.movie_id_lf = m.movie_id AND "
                    + "         (mg.movie_id = m.movie_id AND mg.genre_id = g.genre_id))\n" +
"				GROUP BY m.movie_id";
            
            
            stmt = con.prepareStatement(sql);
            ResultSet rs;
            rs = stmt.executeQuery();
            
            if(rs.next()) {
                
                String add1 = rs.getString("movie_id");
                jLabelID.setText(add1);
                
                String add2 = rs.getString("title");
                jLabelTitulo.setText(add2);
                
                String add3 = rs.getString("genre");
                jLabelGenero.setText(add3);
                
                String add4 = rs.getString("runtime");
                jLabelTempoDura.setText(add4);
                
                String add5 = rs.getString("tempo_assistido");
                jLabelTempoass.setText(add5);
            }
            
            
        } catch (NumberFormatException | SQLException e){
            JOptionPane.showMessageDialog(null, "Erro: " +e);
        }
        
    }//GEN-LAST:event_jTableTabelaUsuarioMouseClicked

